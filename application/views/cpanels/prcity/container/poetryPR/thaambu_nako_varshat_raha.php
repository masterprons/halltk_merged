
<h1 class="text-left pad-l-15px">दे मज</h1>
<pre class="pad-l-15px">
माझे मनची वाचा बोले । दे मज गगनाचे बळ ।  
विशाल अंतराळाचे । दे मज कर्तेपण । ।
 
दे माणूसपण । दे नित्य सुखीचे जन ।
त्यांचे सुख देई मज । स्वानंदाचे संतोष । ।
 
मी तुज आग्रहितो । दे मज खग पंख विशाल । 
सुख: दुखांचे बंध मोकलून । दे मज गगनात विहार । ।
</pre>